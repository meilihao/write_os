#include "cosmostypes.h"
#include "cosmosmctrl.h"

void hal_start()
{
    init_hal(); // 初始化hal层. hal 层把硬件相关的操作集中在这个层，并向上提供接口，目的是让内核上层不用关注硬件相关的细节，也能方便以后移植和扩展
    init_krl(); // 初始化kernel层
    return;
}
